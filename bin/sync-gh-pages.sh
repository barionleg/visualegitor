#!/bin/bash -eu

# This script builds a new gh-pages branch from latest master

cd "$(dirname $0)/.."
git fetch origin
git checkout -B gh-pages origin/master
git reset --hard origin/master

git clean -dffx
# Run npm-install to fetch qunitjs and build dist/
npm install

html='<!DOCTYPE html>
<html lang="en-us">
  <head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1">
    <link rel="stylesheet" type="text/css" href="demos/stylesheets/normalize.css" media="screen">
    <link href="https://fonts.googleapis.com/css?family=Open+Sans:400,700" rel="stylesheet" type="text/css">
    <link rel="stylesheet" type="text/css" href="demos/stylesheets/stylesheet.css" media="screen">
    <link rel="stylesheet" type="text/css" href="demos/stylesheets/light.css" media="screen">
  </head>
  <body>
    <section class="page-header">
      <h1 class="project-name ve-demo-logo-main"></h1>
      <h2 class="project-tagline">Demos</h2>
      <a href="demos/ve/desktop.html" class="btn">VisualEditor desktop demo</a>
      <a href="demos/ve/mobile.html" class="btn">VisualEditor mobile demo</a>
      <a href="demos/trigger/index.html" class="btn">VisualEditor trigger demo</a>
    </section>

    <section class="main-content">
      <footer class="site-footer">
        <span class="site-footer-credits">This page was generated by <a href="https://pages.github.com">GitHub Pages</a> using the <a href="https://github.com/jasonlong/cayman-theme">Cayman theme</a> by <a href="https://twitter.com/jasonlong">Jason Long</a>.</span>
      </footer>

    </section>

  
  </body>
</html>'
echo "$html" > index.html

git add index.html
git add -f node_modules/qunitjs dist/

git commit -m "Create gh-pages branch"
git push origin -f HEAD
